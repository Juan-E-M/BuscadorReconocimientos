    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user = Auth::user();

        // Verifica si el usuario autenticado tiene el rol 'user'
        if ($user && $user->hasRole('user')) {
            // Renderiza la vista con solo los usuarios con el rol 'user'
            return Inertia::render('Users/Index', [
                'users' => User::whereHas('role', function ($query) {
                    $query->where('name', 'user');
                })->get(),
            ]);
        }

        // Si el usuario no tiene el rol 'user', renderiza la vista con todos los usuarios
        return Inertia::render('Users/Index', [
            'users' => User::all(),
        ]);
    }

